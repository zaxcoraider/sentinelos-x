// Generates a Casper ed25519 wallet for each payee agent in the SentinelOS
// team (Commander is the payer, so it keeps the funded main key) and emits the
// COMMITTED public registry packages/agents/src/x402/wallets.ts.
//
// Secret PEMs land in keys/agents/ (gitignored — the registry only carries
// public keys + account hashes, which is what x402 payTo needs).
// Idempotent: existing PEMs are loaded, never overwritten, so account hashes
// stay stable across runs.
//
// Run: node scripts/gen-agent-wallets.mjs
import casperSdk from 'casper-js-sdk';
const { PrivateKey, KeyAlgorithm } = casperSdk;
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const walletDir = join(root, 'keys', 'agents');
mkdirSync(walletDir, { recursive: true });

const ROLES = [
  'risk',
  'oracle',
  'analytics',
  'treasury',
  'governance',
  'compliance',
  'liquidity',
  'insurance',
  'growth',
  'community',
  'legal',
];

const entries = [];
for (const role of ROLES) {
  const pemPath = join(walletDir, `${role}_secret.pem`);
  let key;
  if (existsSync(pemPath)) {
    key = PrivateKey.fromPem(readFileSync(pemPath, 'utf8'), KeyAlgorithm.ED25519);
  } else {
    key = PrivateKey.generate(KeyAlgorithm.ED25519);
    writeFileSync(pemPath, key.toPem());
  }
  const publicKey = key.publicKey.toHex();
  const accountHash = key.publicKey.accountHash().toHex();
  entries.push({ role, publicKey, accountHash });
  console.log(`${role.padEnd(11)} ${publicKey}  account-hash ${accountHash}`);
}

const registry = `// AUTO-GENERATED by scripts/gen-agent-wallets.mjs — do not edit by hand.
// Public wallet registry for the SentinelOS agent team: each specialist agent
// owns its own Casper account and gets paid SOSC over x402 by the Commander.
// Only PUBLIC material lives here; the secret PEMs are gitignored in keys/agents/.

export interface AgentWallet {
  /** Ed25519 public key hex (01-prefixed). */
  publicKey: string;
  /** Bare 32-byte account hash hex — x402 payTo is '00' + this. */
  accountHash: string;
}

export const AGENT_WALLETS: Record<string, AgentWallet> = {
${entries.map((e) => `  ${e.role}: {\n    publicKey: '${e.publicKey}',\n    accountHash: '${e.accountHash}',\n  },`).join('\n')}
};
`;

const outPath = join(root, 'packages', 'agents', 'src', 'x402', 'wallets.ts');
writeFileSync(outPath, registry);
console.log(`\nRegistry written to ${outPath}`);
console.log('Secret PEMs in keys/agents/ (gitignored) — back them up!');
